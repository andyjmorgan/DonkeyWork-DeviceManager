import { Monitor, Smartphone } from 'lucide-react';

interface OSIconProps {
  operatingSystem: string | null;
  size?: number;
  className?: string;
}

function OSIcon({ operatingSystem, size = 20, className = '' }: OSIconProps) {
  if (!operatingSystem) {
    return <Monitor size={size} className={className} />;
  }

  const os = operatingSystem.toLowerCase();

  // Windows
  if (os.includes('windows')) {
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill="currentColor"
        className={className}
      >
        <path d="M0 3.449L9.75 2.1v9.451H0m10.949-9.602L24 0v11.4H10.949M0 12.6h9.75v9.451L0 20.699M10.949 12.6H24V24l-12.9-1.801" />
      </svg>
    );
  }

  // macOS
  if (os.includes('mac') || os.includes('darwin') || os.includes('osx')) {
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill="currentColor"
        className={className}
      >
        <path d="M18.71 19.5c-.83 1.24-1.71 2.45-3.05 2.47-1.34.03-1.77-.79-3.29-.79-1.53 0-2 .77-3.27.82-1.31.05-2.3-1.32-3.14-2.53C4.25 17 2.94 12.45 4.7 9.39c.87-1.52 2.43-2.48 4.12-2.51 1.28-.02 2.5.87 3.29.87.78 0 2.26-1.07 3.81-.91.65.03 2.47.26 3.64 1.98-.09.06-2.17 1.28-2.15 3.81.03 3.02 2.65 4.03 2.68 4.04-.03.07-.42 1.44-1.38 2.83M13 3.5c.73-.83 1.94-1.46 2.94-1.5.13 1.17-.34 2.35-1.04 3.19-.69.85-1.83 1.51-2.95 1.42-.15-1.15.41-2.35 1.05-3.11z" />
      </svg>
    );
  }

  // Linux
  if (os.includes('linux')) {
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill="currentColor"
        className={className}
      >
        <path d="M20.581 19.049c-.55-.446-.336-1.431-.907-1.917.553-3.365-.997-6.331-2.845-8.232-1.551-1.595-1.051-3.147-1.051-4.49 0-2.146-.881-4.41-3.55-4.41-2.853 0-3.635 2.38-3.663 3.738-.068 3.262.659 4.11-1.25 6.484-2.246 2.793-2.577 5.579-2.07 7.057-.237.396-.622.694-1.127.694-1.024 0-.394-2.216-.394-2.216s-1.154 1.556-1.154 2.789c0 .49.123.901.366 1.161.156 1.66 3.524 2.178 6.166 2.178 1.725 0 3.332-.242 4.37-.661 1.127.645 3.196 1.075 5.289 1.075 1.685 0 3.133-.269 4.053-.727.747-.37 1.096-.806 1.096-1.283 0-.499-.536-.754-.862-.972zm-15.916-5.996c-.727.201-1.359.196-1.817-.134.342-1.071.588-2.095 1.223-3.036.341.634 1.115 2.035.594 3.17zm-1.245 3.493c.095-.388.291-.791.625-1.161.164 1.496.653 2.846 1.245 4.058-.393-.23-.788-.531-1.127-.861-.542-1.038-.862-2.036-.743-2.036zm4.24 4.661c-.955-.239-1.788-.561-2.449-.959 1.116-1.191 1.681-2.766 1.844-4.501.293.591.61 1.117 1.069 1.617-.284 1.283-.389 2.539-.464 3.843zm6.979 1.009c-1.374 0-2.758-.373-3.832-.956 1.023-.717 1.641-1.568 1.975-2.501.688.625 1.481 1.127 2.376 1.481-.135.613-.372 1.171-.519 1.976zm1.572-3.993c-.794-.406-1.499-.972-2.125-1.681.352-.628.652-1.308.811-2.041 1.611.982 2.975 2.254 4.107 3.738-1.018-.196-2.058-.435-2.793-1.016zm.984-6.943c.278.061.558.127.842.195-.626 1.038-1.224 2.097-1.887 3.13-.213-.776-.391-1.565-.475-2.371.528-.248 1.033-.508 1.52-.954z" />
      </svg>
    );
  }

  // Android/Mobile
  if (os.includes('android') || os.includes('mobile')) {
    return <Smartphone size={size} className={className} />;
  }

  // Default
  return <Monitor size={size} className={className} />;
}

export default OSIcon;
